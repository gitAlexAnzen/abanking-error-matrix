package mx.com.anzen.app.abanking.error.matrix.jpa.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * <p>TODO [Add comments of the class]</p>
 * 
 * @version abanking-error-matrix
 * @since abanking-error-matrix
 */
@Table(name = "ERROR_MATRIX")
@Entity
public class ErrorMatrix implements Serializable {

	/* serialVersionUID */
	private static final long serialVersionUID = -7118631138220039625L;

	@Id
	@Column(name = "ERROR_CODE")
	private String errorCode;

	@Column(name = "STATUS")
	private String status;

	@Column(name = "MESSAGE")
	private String message;

	/**
	 * @return the errorCode
	 */
	public String getErrorCode() {
		return errorCode;
	}

	/**
	 * @param errorCode the errorCode to set
	 */
	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}

	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}

	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}

	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}

	/**
	 * @param message the message to set
	 */
	public void setMessage(String message) {
		this.message = message;
	}

}
